class Person {
  const Person({
    this.id,
    this.name,
    this.status,
    this.species,
    this.gender,
    this.origin,
    this.location,
    this.image,
  });

  final String? gender;
  final int? id;
  final String? image;
  final String? location;
  final String? name;
  final String? origin;
  final String? species;
  final String? status;
}

